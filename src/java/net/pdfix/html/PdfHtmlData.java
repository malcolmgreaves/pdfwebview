package net.pdfix.html;

////////////////////////////////////////////////////////////////////////////////////////////////////
// Class PdfHtmlData 
////////////////////////////////////////////////////////////////////////////////////////////////////
public class PdfHtmlData {

  public String html = "";
  public String css = "";
  public String js = "";

  void Append(PdfHtmlData data) {
    this.html += data.html;
    this.css += data.css;
    this.js += data.js;
  }

  void Clear() {
    this.html = "";
    this.css = "";
    this.js = "";
  }
}
